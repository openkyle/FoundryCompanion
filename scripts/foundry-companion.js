  static async buildExportPayload() {
    const payload = this.buildWebsitePayload();
    this.prepareExportPayload(payload);
    if (payload.exportOptions.embedImageData) {
      payload.assets = {
        images: await this.collectImageAssets(payload)
      };
      payload.imageDataEmbedded = true;
    }
    return payload;
  }

  static prepareExportPayload(payload) {
    if (Array.isArray(payload.questLog?.sections)) {
      for (const section of payload.questLog.sections) {
        section.questIds = (section.items ?? []).map((quest) => quest.id).filter(Boolean);
        delete section.items;
      }
    }

    for (const section of [payload.journal, payload.contacts, payload.items, payload.characterSheets]) {
      if (!Array.isArray(section?.groups)) continue;
      for (const group of section.groups) {
        group.recordIds = (group.records ?? []).map((record) => record.id).filter(Boolean);
        delete group.records;
      }
    }
  }

  static async collectImageAssets(payload) {
    const urls = Array.from(this.findImageUrls(payload));
    const assets = {};
    for (const url of urls) {
      assets[url] = await this.fetchImageAsset(url);
    }
    return assets;
  }

  static findImageUrls(value, urls = new Set()) {
    if (!value || typeof value !== "object") return urls;

    for (const [key, entry] of Object.entries(value)) {
      if (Array.isArray(entry) && ["imageUrls", "iconUrls", "srcUrls"].includes(key)) {
        entry.filter((url) => typeof url === "string" && url).forEach((url) => urls.add(url));
      } else if (typeof entry === "string" && ["imageUrl", "iconUrl", "srcUrl", "splashUrl"].includes(key) && entry) {
        urls.add(entry);
      } else if (entry && typeof entry === "object") {
        this.findImageUrls(entry, urls);
      }
    }

    return urls;
  }

  static async fetchImageAsset(url) {
    if (url.startsWith("data:")) {
      return {
        url,
        dataUrl: url,
        contentType: url.slice(5, url.indexOf(";")) || "image/*",
        size: url.length,
        embedded: true
      };
    }

    try {
      const response = await fetch(url);
      if (!response.ok) throw new Error(`${response.status} ${response.statusText}`);
      const blob = await response.blob();
      return {
        url,
        dataUrl: await this.blobToDataUrl(blob),
        contentType: blob.type || response.headers.get("Content-Type") || "application/octet-stream",
        size: blob.size,
        embedded: true
      };
    } catch (error) {
      return {
        url,
        dataUrl: "",
        contentType: "",
        size: 0,
        embedded: false,
        error: error.message
      };
    }
  }

  static blobToDataUrl(blob) {
    return new Promise((resolve, reject) => {
      const reader = new FileReader();
      reader.addEventListener("load", () => resolve(reader.result));
      reader.addEventListener("error", () => reject(reader.error));
      reader.readAsDataURL(blob);
    });
  }

  static emptyFolderPayload(key) {
    return {
      folders: [],
      [key]: []
    };
  }

  static companionApiUrl(action) {
    const { endpoint, token } = this.companionConnection();

    return `${endpoint}/${encodeURIComponent(token)}/${action}`;
  }

  static companionConnection() {
    const connectionKey = String(game.settings.get(MODULE_ID, "connectionKey") || "").trim();
    if (connectionKey) return this.parseConnectionKey(connectionKey);

    const endpoint = String(game.settings.get(MODULE_ID, "publishEndpoint") || "").trim().replace(/\/+$/, "");
    const token = String(game.settings.get(MODULE_ID, "publishToken") || "").trim();
    if (endpoint && token) return { endpoint, token };

    throw new Error("Paste the FoundryCompanion connection key generated by the companion website.");
  }

  static parseConnectionKey(connectionKey) {
    if (connectionKey.startsWith(CONNECTION_KEY_PREFIX)) {
      return this.parseEncodedConnectionKey(connectionKey.slice(CONNECTION_KEY_PREFIX.length));
    }

    if (/^https?:\/\//i.test(connectionKey)) {
      return this.parseConnectionUrl(connectionKey);
    }

    throw new Error("Connection key must be an fc1_ key or a full companion /api/foundry-companion/<token> URL.");
  }

  static parseEncodedConnectionKey(encoded) {
    try {
      const normalized = encoded.replace(/-/g, "+").replace(/_/g, "/");
      const padded = normalized.padEnd(Math.ceil(normalized.length / 4) * 4, "=");
      const decoded = JSON.parse(atob(padded));
      const endpoint = String(decoded.baseUrl ?? decoded.apiBaseUrl ?? decoded.endpoint ?? "").trim().replace(/\/+$/, "");
      const token = String(decoded.token ?? decoded.apiToken ?? "").trim();
      if (!endpoint || !token) throw new Error("missing endpoint or token");
      return { endpoint, token };
    } catch (error) {
      throw new Error(`Invalid FoundryCompanion connection key: ${error.message}`);
    }
  }

  static parseConnectionUrl(value) {
    try {
      const url = new URL(value);
      const segments = url.pathname.split("/").filter(Boolean);
      const apiIndex = segments.findIndex((segment, index) => segment === "api" && segments[index + 1] === "foundry-companion");
      if (apiIndex === -1) throw new Error("missing /api/foundry-companion");

      const token = segments[apiIndex + 2];
      if (!token || ["ping", "sync", "data"].includes(token)) throw new Error("missing campaign token");

      const endpointSegments = segments.slice(0, apiIndex + 2);
      const endpointPath = `/${endpointSegments.join("/")}`;
      return {
        endpoint: `${url.origin}${endpointPath}`.replace(/\/+$/, ""),
        token: decodeURIComponent(token)
      };
    } catch (error) {
      throw new Error(`Invalid FoundryCompanion connection URL: ${error.message}`);
    }
  }

  static async testConnection() {
    if (!game.user.isGM) {
      ui.notifications.warn("Only a GM can test FoundryCompanion website connections.");
      return;
    }

    const response = await fetch(this.companionApiUrl("ping"));
    const data = await response.json().catch(() => ({}));

    if (!response.ok || data.ok === false) {
      throw new Error(data.error || `FoundryCompanion ping failed: ${response.status}`);
    }

    ui.notifications.info(`FoundryCompanion connected to: ${data.campaign || "campaign"}.`);
    return data;
  }

