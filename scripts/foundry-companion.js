  static cleanHtml(value) {
    if (!value) return "";
    const div = document.createElement("div");
    div.innerHTML = String(value);
    div.querySelectorAll("script, iframe, object, embed").forEach((node) => node.remove());
    const urlAttributes = new Set(["href", "src", "xlink:href", "formaction"]);
    div.querySelectorAll("*").forEach((node) => {
      [...node.attributes].forEach((attr) => {
        const name = attr.name.toLowerCase();
        const text = String(attr.value ?? "").trim();
        if (/^on/i.test(name)) node.removeAttribute(attr.name);
        if (urlAttributes.has(name) && /^(javascript|vbscript):/i.test(text)) node.removeAttribute(attr.name);
        if (urlAttributes.has(name) && /^data:text\/html/i.test(text)) node.removeAttribute(attr.name);
      });
    });
    return div.innerHTML.trim();
  }

  static htmlImageUrls(html) {
    if (!html) return [];
    const div = document.createElement("div");
    div.innerHTML = String(html);
    return Array.from(new Set(
      Array.from(div.querySelectorAll("img"))
        .map((img) => this.resolveAssetUrl(img.getAttribute("src") ?? ""))
        .filter(Boolean)
    ));
  }

  static resolveHtmlImageUrls(html) {
    if (!html) return "";
    const div = document.createElement("div");
    div.innerHTML = String(html);
    div.querySelectorAll("img").forEach((img) => {
      const src = this.resolveAssetUrl(img.getAttribute("src") ?? "");
      if (src) img.setAttribute("src", src);
    });
    return div.innerHTML.trim();
  }

